import java.util.Scanner;
class LetterComparison {
public static void main(String[] args) {
char letter1 = 'A';
char letter2 = 'B';
System.out.println("Comparing the letters '" + letter1 + "' and '" + letter2 + "':");
System.out.println(letter1 + " < " + letter2 + " is: " + (letter1 < letter2));
System.out.println(letter1 + " <= " + letter2 + " is: " + (letter1 <= letter2));
System.out.println(letter1 + " > " + letter2 + " is: " + (letter1 > letter2));
System.out.println(letter1 + " >= " + letter2 + " is: " + (letter1 >= letter2));
System.out.println(letter1 + " == " + letter2 + " is: " + (letter1 == letter2));
System.out.println(letter1 + " != " + letter2 + " is: " + (letter1 != letter2));
    }
}
